package com.example.echopaw.navigation.model

/**
 * 导航信息加载回调接口
 * 
 * 该接口定义了导航信息异步加载过程中的回调方法：
 * 1. 提供完整的加载生命周期回调
 * 2. 支持泛型以适配不同类型的返回数据
 * 3. 便于处理加载过程中的各种状态
 * 
 * 典型的调用顺序：onStart() -> onSuccess()/onFailed() -> onFinish()
 * 
 * @param <T> 回调数据类型的泛型参数
 */
interface LoadNavigationInfoCallBack<T> {
    
    /**
     * 加载成功回调
     * 
     * 该方法在导航信息加载成功时被调用：
     * 1. 传递加载成功的数据
     * 2. 通知调用方处理成功结果
     * 3. 更新UI或执行后续业务逻辑
     * 
     * @param t 加载成功的数据对象
     */
    fun onSuccess(t: T)
    
    /**
     * 加载开始回调
     * 
     * 该方法在导航信息开始加载时被调用：
     * 1. 通知调用方加载过程已开始
     * 2. 可用于显示加载指示器
     * 3. 执行加载前的准备工作
     */
    fun onStart()
    
    /**
     * 加载失败回调
     * 
     * 该方法在导航信息加载失败时被调用：
     * 1. 通知调用方加载过程失败
     * 2. 可用于显示错误信息
     * 3. 执行错误处理逻辑
     */
    fun onFailed()
    
    /**
     * 加载完成回调
     * 
     * 该方法在导航信息加载完成时被调用（无论成功或失败）：
     * 1. 通知调用方加载过程已结束
     * 2. 可用于隐藏加载指示器
     * 3. 执行清理工作
     */
    fun onFinish()
}
